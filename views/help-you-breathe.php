                    <p>Breathing is something many of us take for granted. With the fad diets and cleanses making the rounds today in the media it's no surprise that almost everyone has heard of a few of them. Yet few people connect breathing and eating, until someone with a common cold has joined you at the dinner table.</p>

<p>When you eat, your body needs increased oxygen in a similar way as if you were doing mild exercise. This is why you need to eat consciously to breathe consciously. Today, I'll explain what I mean by sharing some tips a naturopathic dietician gave my aunt when she was recovering from severe asthma.</p>

<p>It turns out that the old adage that &ldquo;Food Is The Best Medicine&rdquo; is really true. While her inhaler worked well for the sudden attacks, it did nothing to heal or strengthen her lungs. This was something her regular doctor never even considered as a possibility.</p>

<p>The naturopath gave her a specific diet &amp; exercises to strengthen her lungs. While her primary doctor was nice and made sure she prescribed medicine to treat her symptoms, it was the Naturopath that helped my aunt on the path to true healing.</p>

<p>Medical Doctors are trained to believe that there is no cure for chronic ailments and see too many patients in a day to research into the possibility of a cure.  It's part of a traditional MD's day to be bombarded with visits from pharmaceutical companies' reps who leave them with piles of pamphlets on their drugs along with incentives to keep that prescription pad out.</p>

<h1>Eat like your ancestors</h1>

<p>First, we should look at what our ancestors parents knew. Our lungs breathe in and out like a tree. In fact the bronchial pads in our lungs have much in common with the leaves on a tree. A good rule to follow is that in general, eating foods that resemble and breathe like trees is also good for our breathing.</p>
 
<p>Broccoli the vegetable tree is power packed with chlorophyll. This amazing nutrient powers forests around the world. It also has been used to clean the blood, release powerful antioxidants and builds our blood.</p>

<p>Now it may come as a surprise to some but the real test of how well our lungs are working is determined by how rich in oxygen our blood is! It's the blood that carries all the oxygen our heart, brain and body require to live and remain healthy.</p>

<p>At the same time, the more clogged with toxins that our blood gets, the harder it is for our heart to move all that blood. We don't want our veins to get blocked to the point where it becomes difficult for them to transport all the oxygen, vitamins, minerals and other vital nutrients throughout our bodies.</p>

<p>That means that even a small buildup of toxins or hardening of the arteries can impact our blood's ability to move oxygen from the lungs to the brain and so on. All of this means that foods that can improve our blood flow and the ease that our lungs can fill our blood with oxygen has a direct impact on our breathing.</p>

<p>What you also might not know is that for tissue and muscle to heal in the body, oxygen is required.  In fact, the more oxygen rich our blood, the faster we heal. The only cells in the human body that do not need oxygen are cancer cells.</p>

<h1>Brilliant Brassicas</h1>

<p>Broccoli is part of a genus of plants called Brassicas. The members of the Brassica genus are informally known as cruciferous vegetables. Other members of the genus include: cauliflower, Brussels sprouts, cabbage, turnips/turnip greens, collards, kale and bok choy.  These Brassicas work with every bite to make getting the oxygen from our lungs into our blood as easy as possible. These vegetables do all this and more!</p>

<p>Cabbage is great in soups, salads and can be blended into smoothies! Cabbage comes in many colors, shapes and sizes. This vegetable has been shown to slow and fight cancer as well as encourage weight loss.</p>

<p>As wonderful as Brassicas are, they are not the only weapon in your vegetable lung-healing arsenal! If you grew up from the early parts of the 19th century, you might have had a fried onion and garlic poultice placed between tea towels on your chest to fight the flu or pneumonia. There is a reason that this is still used by naturopathic healers today: It works!</p>

<p>But luckily just by eating onions and garlic in salads, soups and other foods provide many benefits as well. Eating onions and garlic (the fresher the better) can reduce inflammation, help your body fight infections, plus they are full of the nutrients that the heart and lungs need to function properly. You see, spices can do more than make our food smell and taste good.</p>

<h1>Lots of different foods can help you</h1>

<p>Ginger for example, is an anti-inflammatory. Just like other soft tissue and muscles in the body the lungs like can become inflamed or swollen when they are wounded. Things as simple as coughing can cause swelling in our lungs which makes it harder to expand our lungs to breathe deeply because they are already swollen. Ginger also has shown its value in encouraging the lungs to cleanse some of the toxins and impurities from them.</p>

<p>Peppers, oh wondrous rainbow colored peppers! Sweet or hot peppers are a powerful medicine. Greens, reds, yellows, and all the other colors even purple are overflowing with capsaicin. This allows them to act as an anti-inflammatory, improve blood flow, and encourage the mucus membranes to get on with their job.</p>

<p>Raw and fresh is best with any of these amazing lung-healing vegetables. But like with all good things, too much of any one of them can confuse the body and even cause it to overreact and treat the good nutrients as an attacking invader.</p>

<p>An onion or two a day will not bother most people but a bag of onions every day for a month is going to reduce the effects of other foods that would normally be providing their own bounty of minerals and nutrients. The same goes for all food. Eat a well-rounded portion of mixed vegetables so that they can work together to heal what that junk food does to our bodies.</p>

<p>When eating meals or snacks, slow down and breathe between bites so our lungs do not have to get a workout along with our stomachs. Be conscious of what you are putting in your body, where it came from and allow yourself the time to taste and enjoy it.</p>

<p>This is how we become Conscious Eaters and Conscious Breathers.</p>


