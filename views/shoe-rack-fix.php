                    <p>Mary Johnson, 42, was at her wit’s end. She loved her three sons more than life itself. They were all bright boys, ranging from freshmen to seniors in high school. But lately she had less-than-pleasant thoughts toward them. She had fantasies about cutting all their feet off.</p>
                    <p>See, her boys were all athletes. Two of them were on the varsity football team, while the third was becoming a great young soccer player. And after practice every day they’d come home, put their shoes on the shoe rack, and fight over who showered first.</p>
                    <p>“That shoe rack,” Mary says, “became the bane of my existence.”</p>
                    <p>The shoe rack was starting to smell. Bad. What’s worse, it was situated on the landing, so you had to pass it when you came into the house.</p>
                    <p>“I swear the smell turned the food rotten when I walked in with groceries,” Mary says. My daughter Katie, she’s eight, she threw up because of the smell. She literally threw up all in the landing. It was that bad.”</p>
