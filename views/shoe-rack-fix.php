                    <p>“I tried everything,” Mary says. “Everything! Baking soda? Nope! Spray? Ha! I even got so desperate that I bought car fresheners and put one in each individual shoe.”</p>
                    <p>With seemingly no way out, Mary’s foot-severing fantasies begun. But little did she know, hope was right around the corner. Literally.</p>
                    <p>“My neighbor knocked on my door and when I opened it she took a step back and gagged,” Mary says. “She asked, ‘Lord what is that smell?’”</p>
