<h1>Negative ions vs. positive ions</h1>

<p>Positive ions are also called cations. They're often created at the same time as negative ions or anions. This is the other half of the Lenard effect we mentioned earlier: positively charged water molecules are created at the same time as negatively charged air molecules.</p>

<p>Positive ions are created by many different processes. During particularly cloudy days, larger amounts of humidity, airborne electrical charges are attracted quicker by higher humidity. Negative ions also quickly attach to airborne particulates in moist air resulting in a denser concentration of positive ions. This encourages a feeling of lethargy.</p>

<p>Positive ions may also influence your feeling of wellbeing for the worse. The 2013 literature review found many people who were exposed to higher than normal levels of positive ions reported more:</p>

<ul>
<li>unpleasantness</li>
<li>acute respiratory irritation</li>
<li>joint symptoms</li>
</ul>

<h1>How negative ions form</h1>

<p>These are oxygen atoms charged with an extra electron. The effects of air, water, sunlight and naturally occurring radiation from the earth create them naturally.</p>

<p>Negatively charged ions are most common in natural places (rural), particularly around moving water or after a thunderstorm.</p>

<p>That taste in the air during and after a storm and the feeling you get near a waterfall or at the beach, is your body assimilating the benefits of negative ions.</p>

<p>The atoms inside molecules have a certain number of electrons floating around a central core, the nucleus. Some electrons are positively charged. Others are negatively charged. This electron balance can be disturbed when a certain amount of energy is applied to the atom. The atom then becomes an air ion.</p>

<p>The atom becomes a positive ion (cation)  if electrons are displaced from the atom. But it becomes a negative ion (anion) if an extra electron is pushed into the atom creating an excessive number of electrons.</p>

<h1>How Do Negative Ionizers Work?</h1>

<p>You have a negative ion generator in your home: the stream of hot water and steam in your shower. This may be the secret behind why so many need a shower to wake up in the morning.</p>

<p>It doesn't help you with the other rooms in your home, though. There's a more practical and effective way to receive the health benefits of negative ions in any room you want from negative ionizers.</p>

<p>Ionizers use a method called 'corona discharge,' which is actually modeled on the way lightning occurs in nature.</p>

<p>A tiny current of electrons flows down a needle to its point. As the electrons come nearer to the needle point, they are forced closer together.</p>

<p>Electrons naturally repel each other, so when they reach the needle's tip, those behind them force them off into the nearest air molecule, and they become a negative ion, scientifically known as an anion.</p>

<p>Negative ions also repel each other, so as more and more are produced, they are spewed farther and farther into the room.</p>

<p>The more powerful the ionizer, the larger the number of beneficial ions they can produce and the wider their range.</p>







