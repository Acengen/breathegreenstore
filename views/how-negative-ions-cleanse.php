                    <p>Negative air ions (NAIs) are not a new discovery &ndash; they were discovered over 100 years ago! NAI generators are currently easily purchased for either home or industrial use.</p>
                    <p>You may know some people who are fanatical about keeping the windows spotless and the floors so clean you could eat off them in their home, but in the end, the air in a house determines its cleanliness. If the air is dirty, everything else inside is too.</p>
                    <p>The highest concentration of unhealthy positive ions is found in polluted cities, crowded areas and in confined spaces such as offices, industrial areas, schools, and cars.</p>
                    <p>Logically, indoor air quality and pollutants represent a health risk for millions of adults and children with allergies, asthma, and other respiratory conditions. Of these, women and children are the primary victims because they spend more time indoors (90% of their working hours are spent indoors). Indoor air pollution has a major effect on the increase in the number of cases of allergic disorders and asthma.</p>

                <h1>
                    Pollutants Indoors
                </h1>

                    <p>Typically, indoor air pollutants are released from dust mites, molds, fungi, bacteria and pests (cockroaches, mice, rats). Common household cleaners and home improvement products release indoor pollutants. Those pollutants trigger asthma and allergy symptoms in children and women.</p>
                    <p>The World Health Organization estimates 1.5 million early deaths yearly can be connected to the indoor air pollution produced from the use of solid fuels. Doing the math, this figure represents over 4,000 deaths per day, more than half of which are children less than 5 years old.</p>

                <h1>
                    Why are children so vulnerable?
                </h1>

                    <p>That number shows just how vulnerable children are to harm from indoor air pollution. Because of their rapid growth rate, infants, as well as young children, maintain a higher metabolic rate at rest combined with an increased rate of oxygen consumption than adults. Thus, the effects of harmful air pollutants can be more significant. Children's airways are smaller in diameter than an adult's. Irritation from inhaling air pollution can create a considerable reaction in a youngster's airways. The same irritant would trigger a minimal response (if any) in an adult. For example, 1 millimeter of edema narrows the diameter of an adult's airway around 19%; compare this to the effect of 56% by the same irritant on an infant's airway.</p>

<p>Negative ion air purifiers help you to breathe cleaner, healthier air and protect yourself from exposure to allergens and viruses. The introduction of negative ions protects you from indoor pollutants such as viruses, dust mites that can make you sick. Actually, the Â­indoor air environment may be as much as five times dirtier than outside.</p>

<p>Indoor air pollution affects children's academic performance lowering concentration levels raising absenteeism caused by asthma and allergies. Dirty inside air weakens youngster's lungs and lowers immunity, which makes them more likely to contract airborne illnesses like colds or flu.</p>

<p>Experiencing headaches, uneasiness, or sudden nausea in an overcrowded room is a common problem. Sometimes even in an air-conditioned room, you might experience these problems.  Reason? Lack of negative ions in the room.</p>

<p>Don't be misled by the name; negative ions aren't harmful or unhealthy. In this case, negative ions are beneficial for your body, and it's the positive ions that are unhealthy. The place you will find the highest concentrations of negative ions in the outdoor environment is naturally clean air.</p>

<p>If you remember your science classes, ions are invisible atoms or molecules in the air. These charged particles carry an electric charge. Some particles have a positive charge, and others bear the negative charge we just mentioned. Simply put, positive ions are molecules that have lost one or more electrons, whereas negative ions are actually oxygen atoms with extra-negatively-charged electrons. Negative ions are abundant in nature, especially around waterfalls, on the ocean surf, at the beach and after a storm. They are widespread in mountains and forests.</p>

<p>Negative ions are found in the air you inhale and are present in your body. Scientific studies suggest negative ions contribute to general wellbeing by:</p>

<ul>
<li>neutralizing free radicals.</li>
<li>boosting cell metabolism.</li>
<li>enhancing immune function.</li>
<li>purifying your blood.</li>
<li>balancing your autonomic nervous system, which regulates deep sleep and healthy digestion.</li>
</ul>

<p>You will find the highest concentration of unhealthy positive ions, in polluted cities, crowded areas and in confined spaces such as offices, industrial areas, schools and cars.</p>

<p>One significant benefit of negative ions is they remove airborne allergens such as mold spores, pollen, viruses and bacteria. They also remove airborne cigarette smoke, dust, and pet dander. Large numbers of negative ions attach to floating pollutants (viruses, bacteria and pollen spores), which become denser and heavier and cannot remain afloat. This prevents you from inhaling them and getting sick. In other words, negative ions act as a protective shield for your health.</p>

<p>At this point, the pollutants attach to a nearby surface or drop to the floor. They are removed from the air you breathe and prevents them from causing respiratory problems, such as asthma and allergies, and even more severe pollution-related illnesses in the long term, like lung cancer.</p>

<p>Unfortunately, your home and workplace are usually closed up tight, removing the natural benefits from negative ions. If you live in a busy urban location, the concentration of airborne negative ions is probably only a tenth that you'd find in the country, even if you do keep your windows open.</p>

<p>Air conditioning, electrical appliances like clothes dryers, televisions, or microwave ovens, and even carpet and upholstery, are all positive ion producers.</p>

<h1>What can you do about it?</h1>

<p>Negative ion air purifiers help you to breathe cleaner, healthier air and protect you from exposure to allergens and viruses. The introduction of negative ions protects you from indoor pollutants such as viruses, dust, and mites, which can make you ill.</p>

<p>Have you felt an upswing in your mood up in the mountains, along the shore, or during a thunderstorm? It's not just the great outdoors. It's probably negative ions, which are electrically charged molecules floating in the air.</p>

<p>Negative ions exist in nature in many places, including:</p>

<ul>
<li>ultraviolet (UV) rays from the sun</li>
<li>airborne electrical discharges after thunder or a lightning strike</li>
<li>anywhere water cascades into water (waterfall or the ocean shore) known as the Lenard effect or spray electrification or the waterfall effect. The friction among water drops in these environments occurs along with the separation of the electric charges.</li>
<li>a product of growth for many plants</li>
</ul>

<p>Here's some of the research into the benefits of negative ionization.</p>

<h1>Benefits of negative ions</h1>

<p>A 2013 review of scientific literature concerning negative ionization, which was published between 1957 and 2012 found ionization improved depression symptoms in some patients.</p>

<p>Exposure to negative ions for several hours may reduce symptoms of depression. Test subjects with chronic depression and seasonal affective disorder (SAD), exposed to a negative ion-rich environment for many hours, reported lower values on survey questions about their depression symptoms. Those suffering from seasonal affective disorder showed symptom improvement after about 30 minutes of exposure.</p>

<p>A very small 2015 study demonstrated a slight improvement in cognitive performance following short periods of exposure to negative ions.</p>

<p>A 2018 review based on 100 years of studies of ionization found negative ionization affects many facets of human health. Researchers found negative ions could:</p>

<ul>
<li>regulate sleep cycles and mood</li>
<li>decrease stress</li>
<li>strengthen the immune system</li>
<li>increase carb and fat metabolism</li>
<li>retard the growth or kill harmful viruses, bacteria, and mold species like Staphylococcus aureus, E. coli, and the tuberculosis bacterium</li>
</ul>

