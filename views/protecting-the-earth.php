<p>We've moved so quickly towards the digital age, as more and more people live their lives behind their smartphone and their gadgets. </p>

<p>Often, we can forget that there is a lot more to life than consuming the latest technology. And when we finally pick our head up away from the phone, we start to see a damaged environment.</p>

<p>Perhaps you also think to yourself: "But what can I do, I'm just one person?"</p>

<p>The great news is that you're asking the right question, and it will help you find the best eco-friendly solutions. </p>

<p>In this article, we'll share our favorite eco-friendly ideas to get you into the swing of things and the bonus is that you can bring your friends and family along, as well. </p>

<h2>Prepare To Be Eco-Friendly</h2>

<p>Any new lifestyle change brings with it a shift in your habits and modes of thinking. And as they say, you have first to be mentally prepared to make the change.</p>

<p>A great way to start this new adventure is to pick a few ideas from our list below that immediately fit in with your lifestyle. </p>

<p>With that said, there are items on this list that really should be non-negotiable. Starting with…</p>

<h2>Switching Off The Lights</h2>

<p>Do you leave the lights on when going to another room for an extended period of time, when you leave the house, or even when you're asleep? Electricity is a non-renewable resource and keeping the lights on unnecessarily uses a great deal of energy over a long period of time. You’ll also notice that this naughty habit leaves is costly on your budget, as it will leave you with an expensive electric bill at the end of the month.</p>

<p>Students at Cornell University saved their school almost $60,000 per year as they started a campaign to switch off the lights when they were not truly needed. </p>

<p>Imagine having everyone in your household apply this just like they did at Cornell. You will undoubtedly see a difference.</p>

<h2>Choose Glass and Other Materials Over Plastic Water Bottles</h2>

<p>Having a water bottle on hand promotes drinking water throughout the day. This trick keeps you refreshed and hydrated, which is critical for your ongoing great health. </p>

<p>BUT, almost 80 percent of water bottles in the United States do not get recycled. </p>

<p>Bottled water is sold everywhere and is thought of as disposable, so people throw it out and buy another bottle. </p>

<p>But the sad and scary reality is that the chemical makeup of plastic bottles is very harmful to the environment and each bottle can take more than 450 years to decompose!</p>

<p>When you choose to use refillable water bottles made from glass or other non-plastic materials instead, you have a supply of water wherever you go and you are doing much less damage to our environment.</p>

<h2>Take A Walk Or Ride A Bike</h2>

<p>This simple step of walking, or riding a bike for shorter trips around town gets cars off the road and allows for less gas being consumed.</p>

<p>Reduce your impact on earth when you choose to walk or invest in a bike to get around. And aside from getting much more exercise, the best part is that you save tons of money on gas costs.</p>

<h2>Reduce Your Meat Intake</h2>

<p>Reducing meat intake is a controversial yet essential topic. The fact remains that we raise livestock for producing extensive amounts of food and this causes a strain on our environment in multiple ways.</p>

<p>Besides the question of inhumanely killing our livestock for our food supply, the waste given off by these animals produces more harmful gases depleting the ozone layer. This impact results in adverse effects from global warming to climate change.</p>

<p>You will see that eating less meat is not such a hard thing and you will even feel better as a result of eating more vegetarian based meals.</p>

<p>Even reducing your meat intake to once per week can have a considerable impact. And when you do it as a family or a social group, the saving is exponential.</p>

<h2>Use Vegan And Cruelty-Free Cosmetics</h2>

<p>Cruelty-Free cosmetics means that ingredients and the products were not tested on animals. Also, vegan cosmetics make the added assertion that no components were animal-derived. </p>

<p>Believe it or not these vegan or cruelty-free products are equally, if not even more effective than traditionally manufactured cosmetic products. And imagine how much more beautiful you will feel knowing that no animals were harmed to created the make-up on your face.</p>

<p>The great news is that more and more brands are going vegan and cruelty-free when it comes to cosmetics.</p>

<p>When shopping for your cosmetics, you can look for the sign on the labels to show it's vegan and cruelty-free.</p>

<h2>Buy A Reusable Grocery Bag</h2>

<p>We're all too familiar with the phrase: "Paper or plastic?". But what if for a moment you decided that your answer could be: Neither.</p>

<p>Many stores now offer a permanent shopping bag to their customers that looks fashionable while also helping the environment due to its reusability.</p>

<p>How’s that for killing two birds with one stone? And if that's not enough, it's super cost-effective too.</p>

<h2>Support Green Stores And Brands</h2>

<p>Make it your choice and your goal to support stores that have decided to go green and help the environment with their business decisions.</p>

<p>You can find these “Green” brands both online and in physical stand alone stores, or in your malls.</p>

<h2>Plant a Simple Backyard Garden</h2>

<p>When we buy fruits and vegetables at the store, it's very convenient. It’s a quick purchase of the food we need and by providing these fruits and vegetables supermarkets make our lives easier.</p>

<p>So we forget about how food originates and the simplicity of growing our food. This simple change can make you value the origins of your diet, knowing what went into producing them.</p>

<p>Plus, by definition your garden fruits and vegetables are not mass produced.</p>

<p>It’s a true feeling of accomplishment and satisfaction when you can literally enjoy the fruits of your labor. And it’s incredibly relaxing, too!</p>

<h2>Buy Second-Hand Goods</h2>

<p>Luckily for you, you're spoiled for choice when it comes to buying goods online. And this goes for pre-owned products, too.</p>

<p>Pre-owned products are also known as “lightly used” and are often practically brand new. You can find second-hand clothing products on Craigslist, Freecycle, or Buynothing groups to buy these “pre-loved” products, usually at a fraction of the cost of your brand-new retail purchases.</p>

<h2>Final Thoughts</h2>

<p>We're not sure about you, but we feel being eco-friendly is both trendy and the right thing to do. We're happy that it's a trend that can make a massive difference to the environment.</p>

<h2>It Starts With YOU</h2>

<p>Make a real difference in your daily habits while helping the environment along the way. </p>

<p>And before you know it, you and everyone around you will have a hand in improving our environment for future generations to come.</p>





