                <div id="articleHeader">
                    Protecting The Earth Starts With YOU
                </div>
                <div id="articleDate">
                    December 13th, 2019 | By Rita Hatthaway
                </div>
                <div class="articleImg">

                    <center><img src="/images/protecting-the-earth.jpg" width="100%" /></center>

