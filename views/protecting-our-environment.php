                    Take A Stand On Protecting Our Environment
                </div>
                <div id="articleDate">
                    December 12th, 2019 | By Rita Hatthaway
                </div>
                <div class="articleImg">

                    <center><img src="/images/protecting-our-environment.jpg" width="100%" /></center>

