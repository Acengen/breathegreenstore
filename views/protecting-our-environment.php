<p>At the recent UN Climate Change Conference COP 25, the world’s biggest polluters did not even make an effort to attend even though our planet is hurtling toward a catastrophic environmental collapse. If our governments can’t (or won’t) get their act together to protect the environment from ongoing abuse, then we all need to take on a strong personal responsibility of protecting Mother Earth.</p>

<p>Socially conscious and environmentally conscious consumers regularly ask us how they can go eco-conscious. First, understand the value of embracing an eco-friendly lifestyle because you need a higher purpose to try and combat the potentially dark days ahead. Protecting the environment is the major reason for adopting an eco-friendly lifestyle, but the additional benefits are infinite. We can all stand to gain extraordinary personal benefits such as health, happiness, and knowing that you are contributing to the overall well-being of all humans on a global scale when it comes to providing immense benefits such as food security for all.</p>

<h2>Protect Our Atmosphere</h2>

<p>A healthy environment can be attained by protecting our planet's atmosphere, hydrosphere, lithosphere and biosphere.</p>

<p>The atmosphere relies primarily on trees, rivers, lakes, and oceans to moderate and maintain the perfect conditions needed to sustain life. Going eco-conscious allows you to contribute to global efforts to protect the atmosphere and attain environmental sustainability. Adopting an eco-friendly lifestyle will enable you to help our planet when you can reduce your carbon footprint by decreasing your energy consumption, waste, and greenhouse gas emissions.</p>

<p>You can reduce your energy consumption by using energy efficient home appliances, machines, equipment, automobiles and electronics.</p> 

<p>You can cut down your greenhouse gas emission by combining several strategies. For example, drive a hybrid car and use natural light during the day instead of electricity. You can also reduce greenhouse gas emissions by decreasing the frequency of your long-distance business travel. You can optimize your schedule by having meetings in locations that reduce traveling back to that location over and over again, which will decrease your greenhouse gas emission drastically, particularly if the meetings require air travel.</p>
 
<h2>Protect Our Hydrosphere</h2>

<p>According to the US National Park Service, banana peels are on the low side of the scale of decomposition because they break down within two to four weeks. However, plastic bottles are at the other end of the scale because they decompose after 450 years, which makes plastic products the biggest polluters of our precious bodies of water on earth. They choke marine life in oceans, lakes, and rivers across the world.</p>
 
<p>To make matters worse, during decomposition plastic bottles gradually release toxic chemicals and other bio-hazardous materials, which compromise the ability of oceans, rivers, and lakes to absorb carbon dioxide (CO2) and release oxygen. Plastic bottles are a threat not only to the health of the planet but also to our existence. You can protect the health of our hydrosphere by disposing of each plastic bottle you use into appropriate recycling bins. Also, you should always use a reusable water bottle and refill instead of buying another bottle of water.</p>

<h2>Protect Our Lithosphere</h2>

<p>The lithosphere refers to soil and elements on the soil surface of the earth, plus it goes down deeper into the earth’s crust and the portion of the upper mantle. The Lithosphere behaves elastically on time scales of thousands of years or greater.</p>

<p>Industrial manufacturing and poor farming techniques degrade soil.  We’ve all heard about garbage and industrial chemicals being dumped into our soil. But a lesser thought of example deals with textiles that are manufactured using natural materials such as cotton or synthetic fibers such as nylon. Believe it or not this can have a negative impact, as well. Cotton farms use strong chemicals to control weeds and pests along with toxic fertilizers, which pollute and degrade the soil. Synthetic fabrics are made using toxic chemicals such as oil, which are later dumped into the ecosystem.</p>

<p>Even though these issues seem to be beyond your control, an example of how you can take back control is by reducing the demand for new textiles by buying second-hand clothes. The increased use of second-hand clothes and even buying recycled clothing are increasingly being adopted as part of an eco-conscious lifestyle.</p>

<h2>Protect Our Biosphere</h2>

<p>Forest cover has been dwindling at an alarming rate contributing to the drastic decline of critical elements such as poor quality of air, scarcity of water, and increasing temperatures. Even though large-scale restoration and conservation of forests and reforestation are gaining traction, there is a need for concerted efforts in tree planting at the grass-root level globally.</p>

<p>Going eco-conscious includes adopting tree planting culture or volunteering for forest conservation and other activities to enhance the health of forests on earth. Reforestation and forest conservation benefits the planet and all living things because healthy forests provide the largest percentage of clean drinking water and oxygen as they absorb CO2, which maintains an ideal condition to support life. The importance of forests goes beyond maintaining the level of oxygen and carbon dioxide in the environment. The additional benefits include filtration and absorption of other pollutants such as carbon monoxide, nitrogen dioxide, sulfur dioxide, and many more!</p>

<h2>Health Benefits of Going Eco-Conscious</h2>

<p>Going eco-conscious is also directly beneficial to your health, happiness, and overall well-being. Living an eco-friendly lifestyle includes changing your diet from mass marketed, mass produced unhealthy fats, sugars, and junk foods to healthy foods such as organic vegetables and fruits, which will enable you to live a healthier life.</p>

<p>In addition to improved health, going eco-conscious will reduce costs in several other potential areas of your life including the prevention of hospital bills and lowered energy bills. If you are a parent, embracing an eco-friendly lifestyle enables you to contribute to the health of future generations by raising children who are environmentally conscious from the start. If each of us embraces this mindset and lifestyle, future generations will have hope that they can live in a more ideal environment with fresh air, clean drinking water, and have plenty of food and resources to fuel their lives.</p>




