<p>Long before the forces of civilization and modernism compelled us to abandon the old practices of our ancestors, humans were but wild animals who thrived on nature. As far as we explore the pages of history and go back in time, we find ourselves exploring lifestyles that were attuned to the bounties of nature. </p>

<p>Today, humans struggle against the forces of climate change, which are quickly depriving us of clean water and air resources, compelling us to limit our time spent in the artificial indoor environments that we have created. </p>

<p>While we could never argue that progress is a good thing, there are also drawbacks of evolution and civilization that have now become more evident than ever.  As humans were designed to thrive in the wild, it is important to study the way of our ancestors to help us feel more attuned to Mother Nature. </p>

<p>Our artificial lives of today can often make us unhappy, sick and unhealthy. Luckily there are ways we can adopt to break free from these vicious cycles and feel our wild selves again. This requires a conscious effort to change or adapting some of our current habits towards strategies that allowed our ancestors to live a healthy and environmentally conscious lifestyle. </p>

<h2 style="text-align:center;margin:0 auto;max-width:600px;">Here are some strategies we can adopt for more conscious and healthy lifestyle:</h2>

<h3>Getting our Nutrients</h3>

<p>Essential nutrients formed a significant part of the ancient diets, but over the years the food processing and manufacturing industry has ridden our diets with harmful chemicals and substances, which have led to the birth of life-threatening diseases like cancer, diabetes, obesity, Alzheimerâ€™s and more. In order to adopt a more conscious pattern of eating, it is important to eliminate all forms of sugar, packaged and processed foods.</p>
 
<p>It is time to replace our unhealthy carbs and trans fats with grains, fiber, and healthy fats, including the disease-fighting omega-3 fatty acids, which we can obtain from wild-caught fish, farm-fed animals and seeds. We must boost our diet with fresh fruits and vegetables, and munch on dried fruits and nuts instead of relying on only store-bought snacks that fill us with empty calories while offering no nutrition whatsoever. </p>

<h3>Physical Training &amp; Exercise</h3>

<p>Before the advent of machinery and modern gadgets, humans relied on their physical abilities to walk, run, hunt and fight. Our modern-day lifestyle, amenities and convenience have promoted a vicious cycle of laziness and lethargy, rendering many of us incapable of getting out of our comfort zones. We need to recognize the importance of getting out of our comfort zones!</p>
 
<p>It is important to nurture and nourish our bodies and mind with a rigorous physical regimen of exercise, mindfulness and fitness that allows us to feel agile and strong. We can adopt any form of exercise that allows us to exhaust both our body and minds. For instance, dance, yoga, Tai Chi, or even workout sessions at the gym. This will help us ultimately feel stronger. </p>

<p>Sports are another excellent avenue to engage both the body and the mind be it tennis, football or any other sport. Those who like to reconnect with nature and feel the sun invigorating their muscles can go on mountain hikes, trail running and climbing. </p>

<h3>Quality Sleep </h3>

<p>Our ancestors would sleep for at least 9-10 hours from a 24-hour day, which allowed them to live longer, fight off chronic illnesses and feel stronger. However, modern-day lifestyles have promoted the prevalence of sleep deprivation. It is important for us to reset the clock, and make sure our bodies enjoy at least 8 hours of sleep a day. </p>

<h3>Turning to Nature </h3>

<p>It is important for us to turn to nature and form a connection that allows us to soothe and invigorate our senses. Nature is the cure for every physiological and psychological ailment, be it a short walk through the park, gardening endeavors, or a scenic trek through the wilderness. Nature allows us to boost the happy hormones in our body and feel more alive as it allows the tranquility and peace to listen to our own thoughts and the voices within.</p>

<h3>Practicing Mindfulness</h3>

<p>It is important to be more aware and mindful of our surroundings. The exhausting every-day routine of modern-day life has compelled us to run after one thing or the other, be it a new fashion trend, an important project at work, or a social event we have been dying to attend.</p>

<p>The traditions of mindfulness seem to be lost to us, but we can reconnect with the ways of our ancestors by making a conscious effort to add beauty to our lives and our surroundings. Meditation is an excellent way to boost our awareness and feel more connected to ourselves and our environment.</p>

<h3>Eliminate the Stress</h3>

<p>Stress is the major culprit behind many of our physical, mental and emotional afflictions. It is the destroyer of our peace and serenity, and it must be eliminated to allow us to grow and thrive as our ancestors did. We must make conscious efforts to eliminate stress and invite happiness into our lives. The pattern is simple. Stress-busting requires essential nutrients, exercise, nature and mindfulness, which would allow us to feel fulfilled and rewarded.</p>



