                What Exactly Is Organic Food And Why Is It Better For You?
						</div>
            <div id="articleDate">
                Thursday, November 28th, 2019 | By Rita Hatthaway
            </div>
            <div class="articleImg">
                <center><img src="/images/what-is-organic-food.jpg" width="100%" /></center>
            </div>





<p>When faced with the difference in price, organic options in the supermarket are definitely more expensive, but the question many ask is, is it really better for us? The answer to that is yes, its definitely better for the body and our planet.</p>

<h3>What farming methods are used today?</h3>

<p>Unlike in the past, today our food is grown with scientifically engineered methods to ensure that huge quantities of perfect crops are grown faster to feed more people. This entails the use of pesticides, herbicides, GMO seeds (bioengineered), petroleum-based and sewage sludge-based fertilizers, and mono-cropping.</p>

<p>Pesticides are used extensively in farming methods to prevent the produce from being spoiled by bugs, but as these are absorbed by the crops they are passed on to all life on earth, our water supplies an eventually, into our bodies.</p>

<p>The decline in the bee populations around the world is directly linked to pesticide use and our planet will face the devastating consequences of losing these natural pollinators needed for plant growth on our farms and in nature.</p>

<p>Herbicides are used to prevent weed growth in the crops and the same as with pesticides they find their way onto our plates and our drinking water.</p>

<p>Mono-cropping is the main reason why these two poisons have become “essential” to crop growth because the method of planting only one crop in endless field makes the soils more susceptible to pests. This farming method also strips soils of their natural nutrients which don't have time to be replaced before the next plot has to be planted. In times past farmers used to rely on crop rotations and resting times to give the soil the ability to recover.</p>

<h3>How is organic food grown?</h3>

<p>Organic food is grown without the use of any of the above farming methods. That means that the food takes longer to grow and more labor-intensive farming methods are needed. As the plants grow, they complete their natural growth cycle and the produce of the crops contain more antioxidants, anthocyanins and flavanols making them more nutritious. With organic farming, plants are fertilized with natural fertilizers like manure and compost.</p>

<p>When it comes to beef, chicken, eggs and more, organic livestock are raised mostly outdoors and are fed on organic feed. They cannot be fed with any animal by-products and are not given antibiotics or growth hormones.</p>

<h3>How come organic food is more expensive?</h3>

<p>As we mentioned above, growing organic food is more labor intensive because weeds have to be pulled out manually, or by machine. Pesticide use is not an option here so farmers have to use complimentary pests and plants that will eradicate the harmful pests that reduce crop production. Some plants naturally produce non-toxic chemicals that ward-off invasive pests that can also be fought off with trap planting, crop rotation, diversified planting and also by introducing more beneficial pests.</p>

<p>All organically grown food has to be specially inspected and certified before it reaches fruit and vegetable markets and stores. This is an expensive process and farmers are faced with penalties even in the event of a slight breach.</p>

<h3>What are the benefits of growing and buying organic food?</h3>

<p>Organically grown foods and food products are associated with a healthier lifestyle and may act as preventative care for our bodies. Buying organically grown food is better because:</p>

<ul>
<li>Our planet is protected against the effects of toxic chemicals through safe farming practices.</li>
<li>Organic food is fresher, especially if bought directly from the producer or from the area where it's grown.</li>
<li>Organic farming helps to reduce pollution, reduces soil erosion, conserves water, and uses less energy.</li>
<li>The animal, bird and insect life in the area of an organic farm are protected from the effects of poisoning.</li>
<li>Organically grown livestock have more space to move around and are healthier.</li>
<li>In the absence of conventional farming the soil retains its vitamins and minerals.</li>
<li>The food is more nutritious and since it contains all its antioxidants it promotes wellness.</li>
<li>Organically grown meat and eggs are also higher in certain nutrients that include omega-3 fatty acids.</li>
<li>People in the agricultural sector are not exposed to the harmful chemicals that place their health in danger.</li>
<li>We might still be able to save the decline of the bee populations across the world if we promote organic farming.</li>
<li>Organically grown food has not had their DNA altered in any way.</li>
</ul>

<h3>Tips on how to eat organic in a cost-effective way?</h3>

<p>Farmers' markets, food co-ops and community farms are generally cheaper and we can buy directly from the producer, cutting out the fee of the middleman that further drives up the pricing.</p>

<h3>Here's a few more tips:</h3>

<ol> 
<li>Seasonal fruits and vegetables are always cheaper and tastier.</li>
<li>Knowing what days produce is delivered allows us to buy fresh.</li>
<li>Comparing prices and checking the frozen organic produce section for cheaper options.</li>
<li>If a product states organic it does not necessarily mean that it's healthy.</li>
</ol>

<p>Checking labels of snacks, desserts and baked goods is essential for those looking for general wellness as these may be packed in fat, salt, and sugar content.</p>

























