<p>Our world is changing and according to scientists and even current political conferences, climate change has to be taken very seriously. Some say it could already be too late.  Many people who care about our planet and wonder what will be left of it for future generations have already started to take action on their own. They have the will to make changes big and small in order to save our planet. In this article, some easily adopted measures for an environmentally friendly lifestyle are presented. These are habits that each and every one of us can begin to live by, starting today.</p>

<h2>Pay Attention To Your Food</h2>

<p>Some of the latest research and official studies have shown dramatic facts concerning the meat and dairy industry as major contributors to the problem of climate change. Not only do these industries fuel global warming with the gassy emissions coming from cows and other livestock, but at the same time a huge amount of land is used for growing food for the meat and dairy industry only.  At first this might sound like nothing but a myth, but sadly it is true.</p>
 
<p>Becoming less reliant on eating animal meat and aiming to adopt more of a plant-based lifestyle can go a long way towards helping moderate the effects of climate change. It’s completely understandable that switching to a total vegan diet could pose a huge challenge. But even if you’re simply reducing the meat you consume, this can still help a lot. Reducing the amount of meat you consume is an obtainable goal and is something everyone can adopt into their everyday lives. And you will be pleasantly surprised how much better you will feel when you eat less meat!</p>

<h2>Reduce Your Use Of Plastic</h2>

<p>We have all heard this over and over again, but still this is a key element of eco-friendly living. Even though the plastic problem is already immense and the governments are lacking any truly game-changing ideas for a real solution, it is still of great importance to not make it worse. We should pay attention to our usage of plastic and reduce it to a minimum. Here are a few simple habits that can lead us closer to a zero-waste lifestyle when it comes to plastic:</p>

<ul>
<li style="list-style-type:circle !important">Bring your own bag for grocery shopping</li>
<li style="list-style-type:circle !important">Buy an aluminum bottle for water refills and avoid plastic bottles</li>
<li style="list-style-type:circle !important">Avoid using plastic forks, knives or spoons and opt for reusable utensils</li>
<li style="list-style-type:circle !important">Shop only with companies that offer eco-friendly shipping when shopping online</li>
</ul>

<p>Reducing our personal use of plastic is actually one of the easier and more achievable targets for eco-friendly living. Although it can be a challenge to stay consistent, a few small changes can  have a massive impact on what you as an individual can do for our planet.</p>

<p>We’ve already done a lot of damage as plastic products are almost everywhere in our environment, especially in our oceans. But if we make the decision as individuals to change our habits, real progress can be made before you know it.</p>

<h2>Planting Trees</h2>

<p>Trees are essential for our climate, as they are considered to be the lungs of our planet. Planting more trees is not only good for clean air and healthy surroundings but at the same time, the process of planting a new tree and helping nurture its growth can be quite therapeutic. There are many local community groups to join and engage with in order to help work toward saving our planet and also finding our way back to nature.</p>

<p>Not everyone has the opportunity to plant trees on their own property, but there are many local, national and international organizations you can work with to plant trees in other locations that need them. Of course if you have the available land and the determination to plant new trees it would be a huge help towards repopulating our planet with what has been taken away through both deforestation and forest fires (even those from natural causes). Those of us with a garden or a similar natural environment should do all we can to engage and help increase the amount of trees in our world again.</p>

<p>Not only is this all a way of eco-friendly living, but it applies to a conscious lifestyle. Connecting with nature is one of the basic principles we could all do better to live by.</p>

<h2>Growing/Making Your Own Food</h2>

<p>This is also a huge part of conscious living. Using natural resources and our ability to create nourishment for ourselves is something we can all strive for. There are a variety of possibilities on how far you want to go to with this, ranging from producing you own food (actually growing vegetables and fruits in the garden) to just making your own homemade dishes instead of going out to dinner, or ordering take-out.</p>

<p>Aim to widen your horizons and embrace the idea of using the sustainable resources nature is giving us. Big supermarkets fill their shelves with products that are mass-produced and not necessarily as healthy as they could be. Or, they are often exploiting local farmers when buying their products as cheap as possible, so as to turn as large a profit as possible.</p>

<p>Many people have begun to change their mindsets now and are taking the time to lovingly create foods like homemade breads, jams, salads, cheeses or other whole foods. It’s a great way to get closer to nature, explore our connection to natural resources and at the same time deliver a huge value in the fight for nature and our planet.</p>

<p>There are many ways we as individuals can follow on our own in order to adopt an eco-friendly and food-friendly lifestyle. This is all closely connected with conscious living and many of us are  already experiencing huge benefits in our lives because of these choices.</p>


