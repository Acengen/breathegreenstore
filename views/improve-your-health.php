<p>Like the last remaining poor group of snow leopards, many have issued the warnings that our planet earth may at the verge of extinction. Mother Nature is being treated vehemently instead of being nurtured and cradled with respect.</p>

<p>Many have observed that in this generation, we need to get back to focusing our attention on the mindset and actions that can preserve our health and that of our world, instead of our current behavior that rids us of good health and tears our world apart.</p>

<h3>What about our environment?</h3>

<p>Do you know that some forms of waste can take as little as 4 years to biodegrade while others might take as many as 400 years?  Do most people care?</p>

<p>Not really!</p>

<p>That's where our Call To Action comes in today. The quality of health we should enjoy and our surroundings should enjoy hinges on the way we treat our health and our planet - earth.</p>

<h3>Environmental Consciousness</h3>

<p>What are some things you can do to save our world through eco-friendliness?</p>

<p>What are some factors that contribute to healthy living?</p>

<p>And how can you be eco-conscious, eco-friendly or environmentally conscious?</p>

<p>These questions will begin to be answered right now!</p>

<p>In order to be environmentally conscious or eco-conscious, we all need to be actively concerned about the health and condition of our world.</p>

<p>We all need to first guard against actions that can deteriorate our immediate environment, in and around our home. Here are some best practices we can all pledge to ourselves that can help reduce the environmental harm to our ecosystem:</p>

<p>1) Be conscious about the effect of your daily habits on your environment. (Take the time to further educate yourself by further researching books and articles like this one.)</p>
 
<p>2) To conserve energy, it's better to wash clothes at a normal temperature. Using hot water to wash your clothes does not what makes them any more sterile. It's a clear waste of energy.</p>

<p>3) Reduce the amount of paper copies you print from your computer. Use and refer to the digital version of documents as often as possible.</p>

<p>4) Remember to always turn off the lights when you don’t need them and when you leave the house. Buy energy efficient bulbs and home appliances.</p>

<p>5) Reuse bottled containers.</p>

<p>6) Plant trees, plants and green other vegetation around your home and community.</p>

<p>7) Avoid littering the environment at all costs. This is a simple choice you can make.</p>

<p>8) Obey the laws and regulations governing waste disposal in your state.</p>

<p>9) Do what you can to help protect wildlife and nature. You’ll soon realize that this does and will directly affect you.</p>

<p>10) Reduce your use of plastic. It's VERY difficult to biodegrade many types of plastic.</p>

<p>The best way to be environmentally conscious is by learning to identify various human behaviors that negatively affect the environment. Take it upon yourself to learn the more eco-friendly techniques and practices. And also remember not to just learn, but to apply what you learn to your daily life!</p>

<p>There are also certain practices you can include into the consciousness of your being that will help you develop a much healthier lifestyle and believe it or not can ultimately also benefit our world at large.</p>

<h3>Meditation</h3>

<p>Everyone ranging from “normal, every-day” people all the way to the highest performing business people, athletes and entertainers talk of the enormous benefits they’ve gained through meditation.</p>

<p>Spend meaningful time with yourself at least once per day to meditate. Start out with as little as 5 minutes per day, but soon aim for at least 20 minutes per day.</p>

<p>Once you learn to mediate and allow your mind to clear and be free of conscious thought, you will be amazed how refreshed and re-energized you will soon feel. Through meditation you’ll also discover how this leads to thinking more clearly throughout your day and thinking more conscientiously about your surroundings and how you treat your family, friends, co-workers and fellow man.</p>

<p>Your meditation practice can create a daily bookmark for you of memory from the depths of your consciousness. This bookmark will eventually create an archive of investments in your soul. Spend the time to learn how to be devoid of thinking about yourself, about your marriage, partner, parents, friends, “frenemies”...  This will result in a form of panacea for your mind, soul and body.</p>

<h3>Why should you aim to temporarily stop your thoughts around yourself during these sections of meditation?</h3>

<p>Note that it's an absolute waste of positive energy when you channel your meditative strength into something that has no possibility of happening to you. The efficient form of meditation is meditation directed into your own soul.</p>

<p>When you cage your mind around your being, you will reduce the flow of thoughts therein and therefore your mind stays more conscious.</p>

<h3>Workouts and Physical Exercise</h3>

<p>In your list of daily activities, physical exercise must never be excluded. The main cause of most health related problem is the dirty embrace we often have with a sedentary lifestyle and a life devoid of routine physical activities. </p>

<p>Make sure you spend time on both meditation and physical exercise. It will be beneficial to devote your mornings to meditation and evenings to physical exercise as a form of “therapy”. If you find that time during the day works better for your schedule then absolutely get your physical activity done at this time, instead of night time.</p>

<p>This routine will result in a win-win situation for both you and your family. On a busy day, even 30 minutes of physical exercise is a goal to strive for. You may simply take a fast walk or jog.</p>

<p>There are no hard and fast rules about this, simply engage yourself in any form of exercise that best suits you.</p>

<p>Physical exercise charges up your soul and mind. It also replenishes you with physical energy and gives vital flow to your internal organs which will become stronger through use.</p>

<h3>Create a healthy food routine and live by it</h3>

<p>Don't be that guy (or girl) who starts the day by jumping on a dining table like an elephant gulps in as much food as it can. It's not about eating as much as you can but rather "as much as you need". It's not about just filling your tummy. It's about nourishing yourself moderately.</p>

<p>Create a mental schedule about the nature and quantity of food you will eat for breakfast, lunch and dinner and stick to it. It requires only a small deal of self-restraint. It would be ideal if our eating decisions were not spontaneous, but rather planned and self-conscious. This is not a call to action to be on a path towards malnourishment. Rather, it is a call to action for conscious living.</p>














