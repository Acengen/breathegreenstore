<p>In today's fast paced society we can all feel at times that life is getting out of hand. It seems like we have 100 things going on at once and it's impossible to focus on what is truly important. But if our focus is always changing… balance doesn't exist.</p>

<p>The truth is we don't have to completely transform to achieve balance in our life. We don't have to become a monk, move to a mountain and mediate 24/7. I mean… Going to this other extreme might work for some, but for many of us it might just be </p>another form of an unsustainable life.

<p>No, we don't have to drastically become monk to find peace, or even to be more conscious. There are small steps that we can take. And the small, simple changes will have a tremendous effect on our lives.</p>

<h2>Meditation</h2>

<p>We may find ourselves living on some different timeline that doesn't correspond to reality. We are either thinking way too much about the future, or thinking of how much we regret what we did in the past.</p>

<p>But what about the present?</p>

<p>Mediation is the key to being present. It is what will allow us to live in the "now".  Sitting still is not easy for many people, but it can prove to be the answer to everything we are looking for.</p>

<p>Meditation allows us to focus on what is going on, but it goes beyond this.</p>

<p>It helps us to stay focused on one task. It allows us to think before we take action and even broadens our perspective of everything we encounter in life.</p>

<p>Even just 10 minutes a day can revolutionize your life. You don't have to run away from everything and hide in a cave. Just sit in your room and be present. It will slow everything down and allow you to be conscious before making any important decisions. It's also surprisingly refreshing and rejuvenating. Through this mediation practice you will soon begin Stage 1 of your journey towards balance in your life.</p>

<h2>Yoga</h2>

<p>When you think of yoga you might think of bearded yogis with long white hair. Or, you might think of women performing really hard stretches. This could be the reality for some, but at the same time there are many other men and women from all walks of life who are experiencing the benefits of yoga.</p>

<p>Yoga focuses both on the body and mind.  Yes at times this could mean pushing yourself through some temporary discomfort, discipline, and focus.  But it is all intended to help bring increased flexibility, strength and "flow" to your body while your thoughts focus on the "now" thus allowing you to pinpoint your energy on the specific situation. </p>

<p>This benefits the body by keeping the joints, tendons and muscles flexible and in optimal health. Plus your focus will become laser-like. Most importantly, you're practicing becoming present and conscious. This both strengthens and relaxes your mind and body each day.</p>

<h2>Plants</h2>

<p>What more pure than something that comes straight from nature? Plants provide us with the oxygen we breathe, but just by having them in our life brings about inner-peace and contributes to our good mood. Have you ever noticed that when you're in a space filled with plants it always brings extra calmness, peacefulness, fulfillment, happiness and delight?</p>

<p>Why not bring that to your every-day environment? Add a couple of plants to your house or office and see the difference it makes. You will soon feel more connected to nature and your roots. Your awareness will grow and you will begin to appreciate the simple things.</p>

<p>You will also be adding mood, health and energy to your environment. You will see your immediate surroundings transform and the good vibes that it brings. Believe me, you will feel better and notice. And as you care for nature this will translate to the care in your life. As you help the earth, so will it help you.</p>

<h2>Essential oils</h2>

<p>Essential oils are another dimension to conscious living that should have a place in your life. They're a natural remedy that can be ingested, inhaled, or applied directly onto the skin. Essential oils are great not only because they come from nature but because they can be enjoyed by a combination of your sensory factors.</p>

<p>Add a diffuser filled with essential oils to invigorate your environment. Whether you need energy, peace or need to be healed, there is an essential oil for the occasion.</p>

<p>Conscious living is all about broadening your awareness and creating an everlasting bond with nature to allow it to bring the highest level of energy and balance to your life.</p>

<p>In today's society we can easily become too attached to media and technology. We rely on them too much for running our lives and forget how to ground ourselves. We also forget how good life is when we are actually in the present.</p>

<p>By adding these 4 simple practices to our daily or weekly routines we can add limitless transformation to our lives.</p>



